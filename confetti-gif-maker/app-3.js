// ── Apply URL params on load ──────────────
(function() {
  const params = new URLSearchParams(location.search);
  if (params.size === 0) return;
  suppressPreview = true;
  Object.keys(DEFAULTS).forEach(k => {
    if (params.has(k)) {
      const v = parseFloat(params.get(k));
      if (!isNaN(v)) {
        const inp = document.getElementById('s_' + k);
        if (inp) { inp.value = v; inp.dispatchEvent(new Event('input')); }
      }
    }
  });
  if (params.has('pal')) {
    params.get('pal').split(',').forEach((v, i) => {
      if (!palette[i]) return;
      if (v === '0') { palette[i].on = false; }
      else { palette[i].hex = v; palette[i].on = true; }
      const el = swDefault.children[i];
      if (!el) return;
      el.style.background = palette[i].hex;
      el.classList.toggle('on', palette[i].on);
      el.classList.toggle('off', !palette[i].on);
      const cinp = el.querySelector('input[type=color]');
      if (cinp) cinp.value = palette[i].hex;
      const xb = el.querySelector('.swatch-x');
      if (xb) xb.style.display = (palette[i].hex !== palette[i].orig) ? 'flex' : 'none';
    });
  }
  if (params.has('cust')) {
    params.get('cust').split(',').forEach((v, i) => {
      if (!customPalette[i]) return;
      const off = v.startsWith('!');
      const hex = off ? v.slice(1) : v;
      customPalette[i].hex = hex;
      customPalette[i].on = !off;
      const slot = swCustom.children[i];
      if (!slot) return;
      slot.style.background = hex;
      slot.childNodes[0].textContent = '';
      slot.classList.add('has');
      slot.classList.toggle('on', !off);
      slot.classList.toggle('off', off);
      const cinp = slot.querySelector('input[type=color]');
      if (cinp) { cinp.value = hex; cinp.style.pointerEvents = 'none'; }
    });
  }
  if (params.has('shapes')) {
    activeShapes = params.get('shapes').split(',');
    document.querySelectorAll('.sb').forEach(btn => {
      btn.classList.toggle('on', activeShapes.includes(btn.dataset.shape));
    });
  }
  if (params.has('mode')) {
    mode = params.get('mode');
    document.querySelectorAll('.mb').forEach(b => {
      b.classList.toggle('on', b.dataset.mode === mode);
    });
  }
  suppressPreview = false;
  history.replaceState(null, '', location.pathname);
})();

